package javaBasics;

public class NumberLiteral
{

	public static void main(String[] args)
	{

		//数値リテラルには整数リテラル(integer literal)、浮動少数点数リテラル(floating-point literal)

		System.out.println( 10 + ":10進数の10");

		System.out.println( 010 + ":8進数で入力するときは先頭に「 0 」をつける;010と入力した");

		System.out.println( 0x10 + ":16進数で入力するときは先頭に「 0x 」をつける;0x10と入力した");

		System.out.println( 0xF + ":0xFと入力した");

	}

}
