package javaChapter0;

public class CharacterAndStringLiteral
{

	public static void main(String[] args)
	{

		System.out.println('C');
		//文字リテラルは['']シングルクオテーションでくくって記述する
		//文字リテラルは英字、数字くらいしか使えないため、特殊な状況以外はダブルクオテーションを使用した方が良さそう

		System.out.println("string");
		//文字リテラルは[""]ダブルクオテーションでくくって記述する
	}

}
